    <script>
  const form = document.getElementById('contact-form');
  const messageContainer = document.getElementById('form-message');
  
  form.addEventListener('submit', (event) => {
    event.preventDefault();
    
    // Send the form data using an AJAX request
    const xhr = new XMLHttpRequest();
    xhr.open('POST', 'submit-form.php');
    xhr.setRequestHeader('Content-Type', 'application/x-www-form-urlencoded');
    xhr.onload = () => {
      if (xhr.status === 200) {
        // Display the success message
        messageContainer.innerText = 'Your message has been sent successfully.';
      } else {
        // Display an error message
        messageContainer.innerText = 'There was an error submitting your message. Please try again later.';
      }
    };
    xhr.send(new FormData(event.target));
  });
</script>

